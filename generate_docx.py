def set_cell_shading(cell, color):
    shading_elm = parse_xml(f'<w:shd {nsdecls("w")} w:fill="{color}"/>')
    cell._tc.get_or_add_tcPr().append(shading_elm)

def add_rtl_paragraph(paragraph, text, bold=False, size=12, color=None, alignment=WD_ALIGN_PARAGRAPH.RIGHT, space_after=6):
    paragraph.alignment = alignment
    run = paragraph.add_run(text)
    run.font.size = Pt(size)
    run.font.name = 'Arial'
    run.bold = bold
    if color:
        run.font.color.rgb = color
    paragraph.paragraph_format.space_after = Pt(space_after)
    # Set RTL
    pPr = paragraph._p.get_or_add_pPr()
    bidi = parse_xml(f'<w:bidi {nsdecls("w")} w:val="1"/>')
    pPr.append(bidi)
    return run

def add_heading_rtl(doc, text, level=1):
    heading = doc.add_heading(level=level)
    heading.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    run = heading.add_run(text)
    run.font.name = 'Arial'
    # Set RTL
    pPr = heading._p.get_or_add_pPr()
    bidi = parse_xml(f'<w:bidi {nsdecls("w")} w:val="1"/>')
    pPr.append(bidi)
    return heading

